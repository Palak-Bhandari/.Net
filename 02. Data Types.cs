// Data types

String name2 = "Palak";
Console.WriteLine("Name (String): " + name2);
int no = 12;
Console.WriteLine("a(int): " + no);
bool bool_var = true;
Console.WriteLine("Variable(boolean): " + bool_var);
char c = 's';
Console.WriteLine("Character(char): " + c);
double d = 12.34;
Console.WriteLine("d(double): " + d);
float f = 12.34F;
Console.WriteLine("f(float): " + f);
dynamic dy = 'w';
Console.WriteLine("dy(dynamic): " + dy);
Object obj = 100;
Console.WriteLine("Obj(Object): " + obj);
Console.WriteLine();
